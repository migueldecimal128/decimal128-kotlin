    @Test
    fun testCases() {
        for (tc in tcs)
            test(tc)
    }

    fun test(tc: TC) {
        if (verbose)
            println(tc)
        val longs = longArrayOf(0xFEED, 0xFACE)
        val observed = Decimal128BidStringCodec.parseReturnError(longs, tc.str)
        if (tc.expectedHex == "invalid") {
            assertNotNull(observed)
            assertEquals(0x7C00000000000000L, longs[0])
            assertEquals(0x0000000000000000L, longs[1])
            return
        }
        assertNull(observed)
        val observedHex = "[${hex16(longs[0])},${hex16(longs[1])}]"
        assertEquals(tc.expectedHex.uppercase(), observedHex)
    }

    private fun hex16(v: Long): String =
        v.toULong().toString(16).padStart(16, '0').uppercase()


    @Test
    fun testParseReturnErrorRejectsTooSmallDest() {
        assertFailsWith<IllegalArgumentException> {
            Decimal128BidStringCodec.parseReturnError(LongArray(1), "0")
        }
    }

